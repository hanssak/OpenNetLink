//left-sidebar  z-index를 낮춰줘야 겹치지 않음

window.initDialog = (popUpId, size) => {
    $("#" + popUpId).dialog({
        height: 'auto',
        minWidth: size,
        maxWidth:1500,
        autoOpen: true,
        draggable: false, //창 드래그 못하게
        modal: true,
        resizable: false,
        position: { my: 'top', at: 'top+100' }
    });
}

window.openDialog = (popUpId) => {
    $("#left-sidebar").css("z-index", "1");
    $("#" + popUpId).css("display", "block");
    $("#" + popUpId).dialog("open");
}

window.closeDialog = (popUpId) => {
    $("#left-sidebar").css("z-index", "1101");
    $("#" + popUpId).dialog("close");
}
